    public String amendPassword(String email, String pass){
        PreparedStatement ps;
        ResultSet rs;
        String query="UPDATE `user` SET `Password`=? WHERE `E-mail`=?";
        
        try {
            ps=MyConnection.getConnection().prepareStatement(query);
            ps.setString(1,pass);
            ps.setString(2,email);
            ps.executeUpdate();
        
        } catch (SQLException ex) {
            Logger.getLogger(forgetPassword.class.getName()).log(Level.SEVERE, null, ex);
        }
                
        return "Check your new Password";
    }
    
    
    
    
