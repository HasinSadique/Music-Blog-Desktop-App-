    private void jButton_ForgetSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ForgetSubmitActionPerformed
        // TODO add your handling code here:
        String email = jTextField_email.getText();
        String newPass = String.valueOf(jPasswordField_newPass.getPassword());
       // boolean x=false;
        if(email.isEmpty()==true || newPass.isEmpty()==true){
            //print "Enter user name or password"
            JOptionPane.showMessageDialog(null, "Enter email address or Password");
        }else{
            //send to DB for verification
        PreparedStatement ps;
        ResultSet rs;
        String query="SELECT * FROM `user` WHERE `E-mail` =?";
            try {
                ps=MyConnection.getConnection().prepareStatement(query);
                ps.setString(1,email);
                rs=ps.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Yes");
                    //x=true;
                    //amend the password
                    String s=amendPassword(email,newPass);
                    JOptionPane.showMessageDialog(null, s);
                }else{
                    JOptionPane.showMessageDialog(null, "No email found");
                }
            } catch (SQLException ex) {
                Logger.getLogger(LoginAndRegistration.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
        
    }//GEN-LAST:event_jButton_ForgetSubmitActionPerformed

